function setup() {
  createCanvas(664, 700);
  // background("#000000");
  // // clear shared object on first user entry
  // if (partyIsHost()) {
  //   partySetShared(shared, {});
  // }
  // init shared colors if they do not exist
  if (!shared.colors) {
    shared.colors = [];
    for (let i = 0; i < 37; i++) {
      shared.colors[i] = { r: 255, g: 255, b: 255 };
    }
  }

  for (let i = 0; i < 19; i++) {
    const { r, g, b } = shared.colors[i];
    colorPickers[i] = createColorPicker(color(r, g, b));
    colorPickers[i].position(550, (i + 1) * 32);
  }
  for (let i = 19; i < 37; i++) {
    const { r, g, b } = shared.colors[i];
    colorPickers[i] = createColorPicker(color(r, g, b));
    colorPickers[i].position(608, (i - 18) * 32);
  }
}

function mousePressed() {
  for (let i = 0; i < 37; i++) {
    console.log(colorPickers[i].color().levels);
    const r = colorPickers[i].color().levels[0];
    const g = colorPickers[i].color().levels[1];
    const b = colorPickers[i].color().levels[2];
    if (r !== 255 || g !== 255 || b !== 255) {
      shared.colors[i] = { r, g, b };
    }
  }
}

